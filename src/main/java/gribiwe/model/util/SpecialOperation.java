package gribiwe.model.util;

/**
 * enum of special operations
 */
public enum SpecialOperation implements Operation {

   /**
    * operation of squaring
    * a number
    */
   SQUARE,

   /**
    * operation of finding
    * root value of number
    */
   ROOT,

   /**
    * operation of dividing
    * number one by another number
    */
   ONE_DIV_X,

   /**
    * operation of negating
    * number
    */
   NEGATE
}
